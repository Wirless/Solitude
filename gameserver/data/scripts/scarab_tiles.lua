function checkScarabTile(pos)
    for _, entry in pairs(SCARAB_TILES) do
        if pos.x == entry.pos.x and pos.y == entry.pos.y and pos.z == entry.pos.z then
			local tile = Tile(pos)
			if os.time() > (tile:getGround():getCustomAttribute("scarabtiletimer") or 0) then
				if math.random(1, 100) <= 95 then
					Game.createMonster("Scarab", pos)
				else
					Game.createItem(2159, 1, pos)
				end
				tile:getGround():setCustomAttribute("scarabtiletimer", os.time() + 4000)
				return
			end
        end
    end
end